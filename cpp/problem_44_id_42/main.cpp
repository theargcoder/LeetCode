class Solution
{
  public:
    int
    trap (std::vector<int> &height)
    {
        int size = height.size ();
        int sum = 0;
        for (int i = 0; i < size - 1; i++)
            {
                if (height[i + 1] < height[i])
                    {
                        int j, max = 0, j_max = 0;
                        bool matched = false;
                        for (j = i + 1; j < size; j++)
                            {
                                if (height[j] > max)
                                    {
                                        max = height[j];
                                        j_max = j;
                                    }
                                if (height[j] >= height[i])
                                    {
                                        matched = true;
                                        break;
                                    }
                            }
                        if (j == size && j_max != 0)
                            {
                                j = j_max;
                                matched = true;
                            }
                        if (matched)
                            {
                                int min = std::min (height[i], height[j]);
                                for (int p = i + 1; p < j; p++)
                                    {
                                        sum += min - height[p];
                                    }
                                i = j - 1;
                            }
                    }
            }
        return sum;
    }
};

