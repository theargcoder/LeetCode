// much much better
class Solution
{
 public:
   std::vector<std::vector<std::string> >
   groupAnagrams (std::vector<std::string> &strs)
   {
      // Use the frequency array as key with our custom hasher.
      std::unordered_map<std::array<int, 26>, std::vector<std::string>,
                         ArrayHash>
          groups;

      for (std::string &word : strs)
         {
            std::array<int, 26> freq = { 0 };
            for (char c : word)
               {
                  ++freq[c - 'a'];
               }
            groups[freq].push_back (std::move (word));
         }

      std::vector<std::vector<std::string> > result;
      result.reserve (groups.size ());
      for (auto &entry : groups)
         {
            result.push_back (std::move (entry.second));
         }

      return result;
   }

 private:
   struct ArrayHash
   {
      size_t
      operator() (const std::array<int, 26> &freq) const
      {
         size_t hashValue = 0;
         // A simple hash combine: multiply previous hash by a prime and add
         // current value.
         for (int count : freq)
            {
               hashValue = hashValue * 31 + std::hash<int> () (count);
            }
         return hashValue;
      }
   };
};

